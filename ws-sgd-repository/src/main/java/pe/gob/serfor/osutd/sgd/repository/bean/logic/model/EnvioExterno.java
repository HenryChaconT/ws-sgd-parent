package pe.gob.serfor.osutd.sgd.repository.bean.logic.model;

public class EnvioExterno {
	private String tiEnvMsj;
	private String nuEmi;
	private String nuAnn;
	private String tipoEnvioExterno;
	private String nomArchivoPrincipal;
	
	public String getNomArchivoPrincipal() {
		return nomArchivoPrincipal;
	}
	public void setNomArchivoPrincipal(String nomArchivoPrincipal) {
		this.nomArchivoPrincipal = nomArchivoPrincipal;
	}
	public String getTiEnvMsj() {
		return tiEnvMsj;
	}
	public void setTiEnvMsj(String tiEnvMsj) {
		this.tiEnvMsj = tiEnvMsj;
	}
	public String getNuEmi() {
		return nuEmi;
	}
	public void setNuEmi(String nuEmi) {
		this.nuEmi = nuEmi;
	}
	public String getNuAnn() {
		return nuAnn;
	}
	public void setNuAnn(String nuAnn) {
		this.nuAnn = nuAnn;
	}
	public String getTipoEnvioExterno() {
		return tipoEnvioExterno;
	}
	public void setTipoEnvioExterno(String tipoEnvioExterno) {
		this.tipoEnvioExterno = tipoEnvioExterno;
	} 
	
	

}
