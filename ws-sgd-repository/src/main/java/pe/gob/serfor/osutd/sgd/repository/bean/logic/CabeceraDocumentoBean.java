package pe.gob.serfor.osutd.sgd.repository.bean.logic;

public class CabeceraDocumentoBean {
	private String numFolio;
	private String fecEmision;
	private String bandejaOrigen;
	private String totalFolios;
	private String asunto;
	private String numEmi;
	private String remitente; 
	private String numExpediente;
	private String estado;
	
	
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getNumExpediente() {
		return numExpediente;
	}
	public void setNumExpediente(String numExpediente) {
		this.numExpediente = numExpediente;
	}
	public String getRemitente() {
		return remitente;
	}
	public void setRemitente(String remitente) {
		this.remitente = remitente;
	}
	public String getNumEmi() {
		return numEmi;
	}
	public void setNumEmi(String numEmi) {
		this.numEmi = numEmi;
	}
	public String getNumFolio() {
		return numFolio;
	}
	public void setNumFolio(String numFolio) {
		this.numFolio = numFolio;
	}
	public String getFecEmision() {
		return fecEmision;
	}
	public void setFecEmision(String fecEmision) {
		this.fecEmision = fecEmision;
	}
	public String getBandejaOrigen() {
		return bandejaOrigen;
	}
	public void setBandejaOrigen(String bandejaOrigen) {
		this.bandejaOrigen = bandejaOrigen;
	}
	public String getTotalFolios() {
		return totalFolios;
	}
	public void setTotalFolios(String totalFolios) {
		this.totalFolios = totalFolios;
	}
	public String getAsunto() {
		return asunto;
	}
	public void setAsunto(String asunto) {
		this.asunto = asunto;
	}
	
	
	

}
