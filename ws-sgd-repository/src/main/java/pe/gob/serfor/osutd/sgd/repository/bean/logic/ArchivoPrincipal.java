package pe.gob.serfor.osutd.sgd.repository.bean.logic;



public class ArchivoPrincipal {
	
	
	private byte[] blDoc;
	private String deRutaOrigen;
	private String coGru;
	
	public String getCoGru() {
		return coGru;
	}
	public void setCoGru(String coGru) {
		this.coGru = coGru;
	}
	public byte[] getBlDoc() {
		return blDoc;
	}
	public void setBlDoc(byte[] blDoc) {
		this.blDoc = blDoc;
	}
	public String getDeRutaOrigen() {
		return deRutaOrigen;
	}
	public void setDeRutaOrigen(String deRutaOrigen) {
		this.deRutaOrigen = deRutaOrigen;
	}
	

}
